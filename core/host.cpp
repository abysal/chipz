    Host::Host() {
        SetTargetFPS(60);
        InitWindow(width * 10, height * 10, "Chip Core");
        this->m_core = std::make_unique<ChipCore>();

        std::jthread thread{ [this] {
            this->m_core->load_rom(chip8_logo, 0x200);
            this->m_core->execute();
        } };

        this->m_emulation_thread.swap(thread);
        this->m_emulation_thread.detach();

        run();
    }

    void Host::run() {
        while (!WindowShouldClose()) {
            BeginDrawing();
            ClearBackground(RAYWHITE);

            const auto cube_width = GetScreenWidth() / width;
            const auto cube_height = GetScreenHeight() / height;

            for (auto x = 0; x < width; x++) {
                for (auto y = 0; y < height; y++) {
                    DrawRectangle(
                        x * static_cast<int>(cube_width),
                        y * static_cast<int>(cube_height),
                        static_cast<int>(cube_width),
                        static_cast<int>(cube_height),
                        PINK
                    );
                }
            }

            EndDrawing();
        }

        this->m_stop = true;
        this->m_emulation_thread.request_stop();

        while (!this->m_core_stopped) {
        }
    }
